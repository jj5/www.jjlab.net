<p>When it comes to electronics lore the most venerable of tomes is
<em><a href="https://hackaday.com/2015/01/29/the-art-of-electronics-third-edition/">The Art of Electronics</a></em>
(on the web at <a href="https://artofelectronics.net/">artofelectronics.net</a>).</p>

<p><a href="https://en.wikipedia.org/wiki/Paul_Horowitz">[Paul Horowitz]</a>
and
<a href="https://en.wikipedia.org/wiki/Winfield_Hill">[W]infield Hill]</a>
first published this masterpiece of electronics literature in 1980. It has been the go-to
book for anyone interested in learning about electronics ever since. The third edition of the book was
published in 2015.</p>

<p><em>The Art of Electronics</em> has two related and supplementary works,
<em><a href="https://hackaday.com/2020/01/23/the-truth-is-in-there-the-art-of-electronics-the-x-chapters/">The Art of Electronics: The x-Chapters</a></em>
(on the web at <a href="https://x.artofelectronics.net/">x.artofelectronics.net</a>) and
<em>Learning the Art of Electronics: A Hands-On Lab Course</em>
(on the web at <a href="https://learningtheartofelectronics.com/">learningtheartofelectronics.com</a>).</p>

<p><em>The x-Chapters</em>, also written by [Horowitz] and [Hill], are a collection of supplementary material that didn't make it into
the third edition of <em>The Art of Electronics</em>.</p>

<p><em>Learning the Art of Electronics</em> is a companion book that provides a structured introduction to
electronics through a collection of 25 lab exercises.</p>

<p>I was saddened recently to find that <em>Learning the Art of Electronics</em> was out of print. I was pleased later to find that
it was only the first edition that was out of print pending the second edition of the book which will be available soon.</p>

<p>The first edition of <em>Learning the Art of Electronics</em> was published in 2016. It was written by
[Thomas C. Hayes] and [Paul Horowitz]. The second edition is written by [Thomas C. Hayes] and
[David Abrams] and will be out this year.</p>

<p>According to the <a href="https://learningtheartofelectronics.com/category/news/">news</a> section of the <em>Learning the Art of Electronics</em> website:</p>

<blockquote>The Second Edition of Learning the Art of Electronics is expected to [be] available in early March 2025. Pre-orders
from the various vendors should commence soon thereafter (the books have to be shipped, un-packed, stocked, etc.)</blockquote>

<p>It's great to see that the second edition of <em>Learning the Art of Electronics</em> is on the way. We here at
<a href="https://hackaday.com/">Hackaday</a> are looking forward to seeing what changes have been made!</p>

<p>For more information about <em>Learning the Art of Electronics</em>, including where to buy, check out the book's homepage at
<a href="https://learningtheartofelectronics.com/">learningtheartofelectronics.com</a>.</p>


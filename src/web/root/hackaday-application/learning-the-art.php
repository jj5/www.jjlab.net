<p>When it comes to electronics lore the most venerable of tomes is
<a href="https://hackaday.com/2015/01/29/the-art-of-electronics-third-edition/">The Art of Electronics</a>
(on the web at <a href="https://artofelectronics.net/">artofelectronics.net</a>).</p>

<p><a href="https://en.wikipedia.org/wiki/Paul_Horowitz">Paul Horowitz</a>
and
<a href="https://en.wikipedia.org/wiki/Winfield_Hill">Winfield Hill</a>
first published this masterpiece of electronics literature in 1980. It has been the go-to
book for anyone interested in learning about electronics ever since. The third edition of the book was
published in 2015.</p>

<p>The Art of Electronics has two related and supplementary works,
<a href="https://hackaday.com/2020/01/23/the-truth-is-in-there-the-art-of-electronics-the-x-chapters/">The Art of Electronics: The x-Chapters</a>
(on the web at <a href="https://x.artofelectronics.net/">x.artofelectronics.net</a>) and
<strong>Learning the Art of Electronics: A Hands-On Lab Course</strong>
(on the web at <a href="https://learningtheartofelectronics.com/">learningtheartofelectronics.com</a>).</p>

<p>The x-Chapters, also written by Horowitz and Hill, are a collection of supplementary material that didn't make it into
the third edition of The Art of Electronics.</p>

<p>Learning the Art of Electronics is a companion book that provides a structured introduction to
electronics through a collection of 25 lab exercises.</p>

<p>I was saddened recently to find that Learning the Art of Electronics was out of print. I was pleased later to find that
it was only the first edition that was out of print pending the second edition of the book which will be available soon.</p>

<p>The first edition of Learning the Art of Electronics was published in 2016. It was written by
Thomas C. Hayes and Paul Horowitz. The second edition is written by Thomas C. Hayes and
David Abrams and will be out this year.</p>

<p>According to the <a href="https://learningtheartofelectronics.com/category/news/">news</a> section of the Learning the Art of Electronics website:</p>

<blockquote>The Second Edition of Learning the Art of Electronics is expected to [be] available in early March 2025. Pre-orders
from the various vendors should commence soon thereafter (the books have to be shipped, un-packed, stocked, etc.)</blockquote>

<p>It's great to see that the second edition of Learning the Art of Electronics is on the way. We here at
<a href="https://hackaday.com/">Hackaday</a> are looking forward to seeing what changes have been made!</p>

<p>For more information about Learning the Art of Electronics, including where to buy, check out the book's homepage at
<a href="https://learningtheartofelectronics.com/">learningtheartofelectronics.com</a>.</p>


function get_latest_silly_job_title() {

  $result = null;

  foreach ( get_list( Show::class ) as $show ) {

    $silly_job_title = $show->get_first_segment()->get_silly_job_title();

    if ( $silly_job_title === null ) { continue; }

    if ( $silly_job_title->is_null() ) { continue; }

    $result = $silly_job_title;

  }

  return $result;

}

function render_youtube_iframe( $youtube_id, $class = 'video-frame' ) {

  foreach (
    [
      'rc-base' => [
        'width' => 320,
        'height' => 180,
      ],
      'rc-0320' => [
        'width' => 320,
        'height' => 180,
      ],
      'rc-0640' => [
        'width' => 560,
        'height' => 315,
      ],
      'rc-1280' => [
        'width' => 640,
        'height' => 360,
      ],
      'rc-1920' => [
        //'width' => 1280,
        //'height' => 720,
        'width' => 640 + ( 640 / 2 ),
        'height' => 360 + ( 360 / 2 ),
      ],
    ]
    as $rc => $spec
  ) {

    tag_open( 'div', [ 'class' => "$rc $class" ] );

      tag_bare(
        'iframe',
        [
          'data-src' => "https://www.youtube.com/embed/$youtube_id",
          'allow' => "accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture",
          'width' => $spec[ 'width' ],
          'height' => $spec[ 'height' ],
          // 2024-06-29 jj5 - this is obsolete...
          //'frameborder' => 0,
          'allowfullscreen' => true,
        ]
      );

    tag_shut( 'div' );

  }
}


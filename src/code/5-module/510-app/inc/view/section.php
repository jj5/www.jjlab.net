function get_latest_show() {

  $result = null;

  foreach ( get_list( Show::class ) as $show ) {

    $segment = $show->get_first_segment();

    if ( $show->get_channel()->get_channel_slug()->to_string() !== '@InTheLabWithJayJay' ) { continue; }

    if ( $segment->is_live() ) { $result = $segment; }

  }

  return $result;

}

